package RelationShipTypes;

public class Answer {

	private int ansno;
	private String answer;
	public Answer(int ansno, String answer) {
		super();
		this.ansno = ansno;
		this.answer = answer;
	}
	public int getAnsno() {
		return ansno;
	}
	public void setAnsno(int ansno) {
		this.ansno = ansno;
	}
	public String getAnswer() {
		return answer;
	}
	public void setAnswer(String answer) {
		this.answer = answer;
	}
	
	
}
